function suma(op1, op2, op3){
    return op1 + op2 + op3
}
function resta(op1, op2){
    return op1 - op2
}
function multiplica(op1, op2){
    return op1 * op2
}
function divide(op1, op2){
    return op1 / op2
}
function cuadrado(n){
    return n * n
}
module.exports = {suma, resta, multiplica, divide,cuadrado}